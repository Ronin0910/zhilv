def build_context_string(docs: list)-> str:
    """把检索到的文档列表拼成一个字符串"""
    parts = []
    for i, doc in enumerate(docs):
        source = doc.metadata.get("source","未知来源")
        parts.append(f"[来源: {source}]\n{doc.page_content}")
    return "\n\n---\n\n".join(parts)